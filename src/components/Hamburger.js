import React from 'react';
import Button from '@material-ui/core/Button';
import Menu from '@material-ui/core/Menu';
import MenuItem from '@material-ui/core/MenuItem';
import MenuRoundedIcon from '@material-ui/icons/MenuRounded';

const Hamburger = () => {
    const [anchorEl, setAnchorEl] = React.useState(null);

    const handleClick = (event) => {
        setAnchorEl(event.currentTarget);
    };

    const handleClose = () => {
        setAnchorEl(null);
    };

    return (
        <div>
            <Button aria-controls="simple-menu" aria-haspopup="true" onClick={handleClick} className="HamburgerButton">
                <MenuRoundedIcon />
            </Button>
            <Menu
                id="simple-menu"
                anchorEl={anchorEl}
                keepMounted
                open={Boolean(anchorEl)}
                onClose={handleClose}
                className="HamburgerMenu"
            >
                <MenuItem onClick={handleClose}><a className="HeaderHeading" href="https://highright.herokuapp.com/" target='_blank'>ALL HIGHRIGHTS</a></MenuItem>
            </Menu>
        </div>
    );
}

export default Hamburger;